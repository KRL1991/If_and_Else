package If_Else;

public class If_and_Else {
    // Eksempel på If - Else statement
    public static void main(String[] args) {

        int x = 75;
        int y = 95;
        if (x > y) {
            System.out.println("X is greater than Y");
            // Hvis X er større end Y printes ovenstående sætning

            // Hvis Y er større printes der nedenstående sætning
        } else {
            System.out.println("X is not greater than Y.");
        }
    }

}
